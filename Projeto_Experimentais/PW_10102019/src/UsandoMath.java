public class UsandoMath {
    public static void main(String[] args) {
        System.out.println("random: "+Math.random()*6);
        System.out.println("round: "+Math.round(3.5));//arredonda pra cima ou pra baixo
        System.out.println("floor: "+Math.floor(3.4));//arredonda o inteiro menor mais proximo
        System.out.println("ceil: "+Math.ceil(3.4));//arredonda o inteiro maior mais proximo
        System.out.println("sqrt: "+Math.sqrt(9));//calcular raiz quadrada
        System.out.println("pow: "+Math.pow(3,2));//calcular potencia
    }
}
