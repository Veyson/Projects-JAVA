public class Calculadora {

    float somar(float v1, float v2) {
        return v1 + v2;
    }

    float subtrair(float v1, float v2) {
        return v1 - v2;
    }

    float multiplicar(float v1, float v2) {
        return v1 * v2;
    }

    float dividir(float v1, float v2) {
        return v1 / v2;
    }
}
