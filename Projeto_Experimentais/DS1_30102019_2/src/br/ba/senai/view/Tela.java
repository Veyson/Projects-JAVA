    private void Inserir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Inserir
        // TODO add your handling code here:
        if(ck.isSelected()){
            String fruta = JOptionPane.showInputDialog("Digite a fruta:");
            cbFruta.addItem(fruta);
            model.addElement(fruta);
        }else{
            cbFruta.addItem(tfFruta.getText());
            model.addElement(tfFruta.getText());
        }
        tfFruta.setText("");
        tfFruta.requestFocus();
    }//GEN-LAST:event_Inserir

    private void Excluir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Excluir
        // TODO add your handling code here:
        int op = JOptionPane.showConfirmDialog(null, "Deseja excluir o item?", "exemplo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(op==0){
            cbFruta.removeItemAt(cbFruta.getSelectedIndex());
            model.removeElementAt(listaFruta.getSelectedIndex());
        }
    }//GEN-LAST:event_Excluir

